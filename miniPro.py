def คำนวณความเร็วเวลาและปริมาณค่าน้ำมัน():
    print("")
    print("======== คำนวณความเร็ว เวลา ปริมาณและค่าน้ำมันตามระยะทาง ========")
    try:
        ระยะทาง = float(input("ป้อนระยะทาง (กิโลเมตร): "))
        ความเร็ว = float(input("ป้อนความเร็ว (กิโลเมตรต่อชั่วโมง): "))
        if ความเร็ว <= 0:
            print("ความเร็วต้องมากกว่า 0")
            return

        # คำนวณเวลา
        เวลา = ระยะทาง / ความเร็ว
        print(f"เวลาที่ใช้ในการเดินทาง: {เวลา:.2f} ชั่วโมง")

        # คำนวณปริมาณน้ำมัน
        ปริมาณน้ำมัน = ระยะทาง / 12  # 12 กิโลเมตรต่อลิตร
        print(f"ปริมาณน้ำมันที่ใช้ในการเดินทาง: {ปริมาณน้ำมัน:.2f} ลิตร")

        # คำนวณค่าน้ำมัน
        ราคาน้ำมัน = float(input("ป้อนราคาน้ำมัน (บาทต่อลิตร): "))
        if ราคาน้ำมัน < 0:
            print("ราคาน้ำมันต้องไม่เป็นลบ")
            return
        print("")
        ค่าน้ำมัน = ปริมาณน้ำมัน * ราคาน้ำมัน
        print(f"ค่าน้ำมันที่ใช้ในการเดินทาง: {ค่าน้ำมัน:.2f} บาท")
    except ValueError:
        print("")
    print("============================================================")

def คำนวณความเร็วรถเป็นรอบต่อนาทีของล้อต่อนาที():
    print("")
    print("=========== คำนวณความเร็วรถเป็นรอบต่อนาทีของล้อต่อนาที ===========")
    # รับค่าความเร็วรถจากผู้ใช้
    distance = float(input("กรุณากรอกระยะทางที่ล้อหมุน (เมตร): "))
    time = float(input("กรุณากรอกเวลาในการหมุน (วินาที): "))

    if time <= 0:
        print("เวลาไม่สามารถเป็นศูนย์หรือค่าลบได้")
        return

    speed_mps = distance / time  # คำนวณความเร็ว (เมตรต่อวินาที)
    wheel_circumference = 0.7  # เส้นรอบวงของล้อ (เมตร)
    wheel_speed_rpm = (speed_mps / wheel_circumference) * 60  # แปลงเป็นรอบต่อนาที
    print("")
    print(f"ความเร็วรอบของล้อรถคือ {wheel_speed_rpm:.2f} รอบต่อนาที")
    print("==========================================================")

def คำนวณแรงม้า():
    print("")
    print("===================== ฟังก์ชันคำนวณแรงม้า =====================")

    # รับค่าน้ำหนักรถและเวลา
    weight = float(input("กรุณากรอกน้ำหนักรถ (ปอนด์): "))
    time_seconds = float(input("กรุณากรอกเวลา (วินาที): "))

    # คำนวณแรงม้า
    horsepower = weight / ((time_seconds / 5.825) ** 3)
    print("")
    print(f"แรงม้า: {horsepower:.2f} แรงม้า")
    print("==========================================================")


